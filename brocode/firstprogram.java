public class firstprogram{
    public static void main(String[] args) {
        System.out.println("Hi,this is my first program in java");
        System.out.println("\tI love tech");
        // For a same effect as println line statement we can use escape sequence that is \n
        System.out.println("It is interesting\n");
        // For using double quotes in print statement you have to surround it with / .
        System.out.println("\"I love my family\"");
    }
}
